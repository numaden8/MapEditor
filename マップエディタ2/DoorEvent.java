public class DoorEvent extends Event {

    /**
     * @param x X座標
     * @param y Y座標
     */
    public DoorEvent(int x, int y) {
        // とびらのチップ番号は195でぶつかる
        super(x, y, 195, true);
    }
    
    /**
     * イベントを文字列に変換（デバッグ用）
     */
    public String toString() {
        return "ENEMY," + super.x + "," + super.y;
    }
}
